let size = "S";
// Tulis kode switch statement di sini

switch (size) {
  case "S":
    console.log(`Size ${size}: Small (36-38)`);
    break;

  case "M":
    console.log(`Size ${size}: Medium (40-42)`);
    break;

  case "L":
    console.log(`Size ${size}: Large (44-46)`);
    break;

  case "XL":
    console.log(`Size ${size}: Extra Large (48-50)`);
    break;

  default:
    console.log("Size tidak tersedia");
    break;
}
