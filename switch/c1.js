let nomorHari = 3;
// Tulis kode switch statement di sini

switch (nomorHari) {
    case 1:
        console.log(`Hari ke-${nomorHari}: Senin`)
        break;

    case 2:
        console.log(`Hari ke-${nomorHari}: Selasa`)
        break;

    case 3:
        console.log(`Hari ke-${nomorHari}: Rabu`)
        break;

    case 4:
        console.log(`Hari ke-${nomorHari}: Kamis`)
        break;

    case 5:
        console.log(`Hari ke-${nomorHari}: Jumat`)
        break;

    case 6:
        console.log(`Hari ke-${nomorHari}: Sabtu`)
        break;
    
    case 7:
        console.log(`Hari ke-${nomorHari}: Minggu`)
        break;
        
    default:
        break;
}